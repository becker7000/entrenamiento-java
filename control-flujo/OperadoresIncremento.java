public class OperadoresIncremento {

    public static void main(String[] args) {

        int x = 10;
        System.out.printf("\n\t x = %d",x++); // Postincremento
        System.out.printf("\n\t x = %d",x);
        System.out.printf("\n\t x = %d",++x); // Preincremento

    }

}


/*
    x++ post-incremento
    ++x pre-incremento
    x-- post-decremento
    --x pre-decremento
 */