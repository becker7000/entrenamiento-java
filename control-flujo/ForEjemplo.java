public class ForEjemplo {

    public static void main(String[] args) {

        for(int i=1; i<=10; i++){
            System.out.println("\n\t i = "+i);
        }

    }

}

/*
*  Todos los bucles cuentan con 3 elementos:
*
*  1. Variable de control (int i=1)
*  2. Condición (expresión lógica/relacional) (i<=10)
*  3. Incremento/Decremento (i++)
*
* */