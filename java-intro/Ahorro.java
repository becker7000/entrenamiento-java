public class Ahorro {

    public static void main(String[] args) {

        // Todas las sumas inician en cero
        int ahorro = 0;

        ahorro+=150; // 150
        ahorro+=250; // 400
        ahorro+=100; // 500
        ahorro+=200; // 700
        ahorro*=2; // 1400

        System.out.printf("\n\t El ahorro final es: $%d",ahorro);

    }

}
